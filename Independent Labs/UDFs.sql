Snowflake currently supports two types of UDFs, SQL and JavaScript: A SQL UDF evaluates an arbitrary SQL expression and returns either scalar or tabular results. A JavaScript UDF lets you use the JavaScript programming language to manipulate data and return either scalar or tabular results.

Snowflake does not place any hard limits on the number of shares you can consume from data providers; however, you can only create one database per share. For more details, see Data Consumers.

Transient tables can have a Time Travel retention period of either 0 or 1 day. Temporary tables can also have a Time Travel retention period of 0 or 1 day; however, this retention period ends as soon as the table is dropped or the session in which the table was created ends.

